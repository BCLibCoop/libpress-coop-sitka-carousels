<p>
    Sitka Carousels can be added to this Highlight by inserting one or more shortcodes into the Highlight text. Shortcodes take the following format:
    <br /> <strong>[sitka_carousel type="adult_fiction" transition="fade"]</strong>
</p>
<p>Possible values for type: adult_fiction, adult_nonfiction, adult_largeprint, adult_dvds, adult_music, teen_fiction, teen_nonfiction, juvenile_fiction, juvenile_nonfiction, or juvenile_dvdscds. Defaults to adult_fiction.</p>
<p>Possible values for transition are fade or swipe. Defaults to fade.</p>
<p>More than one carousel shortcode can be added to a Highlight.</p>
